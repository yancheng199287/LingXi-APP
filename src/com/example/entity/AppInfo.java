package com.example.entity;

public class AppInfo {

	private String helpCenter;
	private String saleActivity;
	private String url;
	private String version;
	private String about;
	public String getHelpCenter() {
		return helpCenter;
	}
	public void setHelpCenter(String helpCenter) {
		this.helpCenter = helpCenter;
	}
	public String getSaleActivity() {
		return saleActivity;
	}
	public void setSaleActivity(String saleActivity) {
		this.saleActivity = saleActivity;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getAbout() {
		return about;
	}
	public void setAbout(String about) {
		this.about = about;
	}
	
	
	
}
